# Numbers

#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    // power function
    cout << pow(2,5) << endl;
    
    
    // sqrt function
    cout << sqrt(36) << endl;
    
    // round function 
    cout << round(35.2) << endl;
    
    // round up function
    cout << ceil(35.2) << endl;

    // round down function
    cout << floor(35.2) << endl;
    
    // prints out bigger number
    cout << fmax(3,10) << endl;
    
    // prints out smaller number
    cout << fmin(3,10) << endl;
    
    return 0;
}
