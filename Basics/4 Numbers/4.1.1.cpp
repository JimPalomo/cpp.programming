// Numbers: operations, printing, mod

#include <iostream>

using namespace std;

int main()
{

    // Printing numbers 
    cout << -40.1232133 << endl;
    
    // Mathematical operations
    cout << 7 / 5 << endl;      // numbers are rounded to assigned variables (int = whole numbers)
    cout << 5 * 5 << endl;
    cout << 1 + 2 << endl;
    cout << 20 - 5 << endl;
    cout << (20 + 1) * 10;
    cout << "\n";
    
    // Mod operator
    cout << 10 % 3;

    return 0;
}
